package MonticuloBinario;

/**
 *
 * @author Gabriel
 */
public class Nodo {
    private int numero;
    Usuario usuario;
    private Nodo left, right;

    public Nodo(int numero, Usuario usuario) {
        this.numero = numero;
        this.usuario = usuario;
        this.left = null;
        this.right = null;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public Usuario getUsuario() {
        return usuario;
    }

    public void setUsuario(Usuario usuario) {
        this.usuario = usuario;
    }

    public Nodo getLeft() {
        return left;
    }

    public void setLeft(Nodo left) {
        this.left = left;
    }

    public Nodo getRight() {
        return right;
    }

    public void setRight(Nodo right) {
        this.right = right;
    }
    
    
}
